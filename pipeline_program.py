def resolve_world_concept(world_db, surface, query_intent=None):
    normalized = normalize_surface_name(surface)
    record = world_db.get("concept_registry", {}).get(normalized)
    if not record:
        return {
            "status": "not_found",
            "action": "retrieve_source",
            "concept_ids": [],
        }
    if query_intent:
        route = record.get("intent_routes", {}).get(query_intent)
        if route and route.get("resolution") == "resolved":
            return {
                "status": "resolved",
                "action": "use_concept_id",
                "concept_ids": route["candidate_ids"],
                "query_intent": query_intent,
            }
        if route:
            return {
                "status": "ambiguous",
                "action": "use_graph_context_or_retrieve_source",
                "concept_ids": route["candidate_ids"],
                "query_intent": query_intent,
            }
    if record.get("default_concept_id"):
        return {
            "status": "resolved",
            "action": "use_concept_id",
            "concept_ids": [record["default_concept_id"]],
            "query_intent": query_intent,
        }
    return {
        "status": "ambiguous" if record.get("requires_intent") else "unresolved",
        "action": (
            "supply_query_intent"
            if record.get("requires_intent")
            else "retrieve_source_then_runtime_resolve"
        ),
        "concept_ids": [
            item["concept_id"] for item in record.get("candidates", [])
            if item.get("runtime_eligible")
        ],
    }


def evaluate_world_action(world_db, action):
    required = {"actor_id", "action_type", "target_ids", "parameters"}
    missing = sorted(required - set(action))
    if missing:
        return {
            "decision": "invalid_action_query",
            "missing_fields": missing,
            "applicable_rules": [],
        }
    candidate_rule_ids = set(action.get("candidate_rule_ids", []))
    actor_id = action["actor_id"]
    target_ids = set(action.get("target_ids", []))
    rules = []
    for rule in world_db.get("rule_engine", {}).get("rules", []):
        entity_match = bool(
            actor_id in set(rule.get("constrains", []))
            or actor_id in set(rule.get("applies_to", []))
            or target_ids & set(rule.get("constrains", []))
            or target_ids & set(rule.get("applies_to", []))
        )
        if rule["rule_id"] in candidate_rule_ids or entity_match:
            rules.append(rule)
    if not rules:
        return {
            "decision": "unknown_not_prohibited",
            "applicable_rules": [],
            "commit_allowed": False,
            "reason": "No evidence-backed applicable rule was selected.",
        }
    if any(
        rule["model_status"] == "trusted"
        and rule["enforcement"] == "hard_block"
        for rule in rules
    ):
        decision = "blocked"
    elif any(rule["model_status"] == "supported" for rule in rules):
        decision = "uncertain_requires_confirmation"
    elif any(
        rule["enforcement"] == "requires_runtime_review" for rule in rules
    ):
        decision = "requires_runtime_review"
    elif any(
        rule["enforcement"] == "soft_constraint" for rule in rules
    ):
        decision = "allowed_with_consequence"
    else:
        decision = "unknown_not_prohibited"
    return {
        "decision": decision,
        "commit_allowed": decision == "allowed_with_consequence",
        "applicable_rules": [rule["rule_id"] for rule in rules],
        "evidence": [
            item for rule in rules for item in rule.get("evidence", [])
        ],
    }


def validate_world_claim(world_db, claim):
    required = {"speaker_id", "subject_concept_id", "predicate", "object_or_value"}
    missing = sorted(required - set(claim))
    if missing:
        return {"outcome": "invalid_claim", "missing_fields": missing}
    concept_status = {
        candidate["concept_id"]: candidate["model_status"]
        for record in world_db.get("concept_registry", {}).values()
        for candidate in record.get("candidates", [])
    }
    subject_status = concept_status.get(claim["subject_concept_id"])
    if subject_status in {None, "rejected"}:
        return {
            "outcome": "invalid_concept_reference",
            "correction_action": "block_and_resolve_concept",
        }
    if subject_status == "unresolved":
        return {
            "outcome": "unknown_requires_retrieval",
            "correction_action": "retrieve_source_then_retry",
        }
    if subject_status == "supported":
        return {
            "outcome": "supported_uncertain",
            "correction_action": "qualify_claim_and_attach_evidence",
        }
    state = world_db.get("world_state", {}).get(
        "entity_states", {}
    ).get(claim["subject_concept_id"], {})
    predicate = claim["predicate"]
    if predicate.startswith("state."):
        field = predicate.split(".", 1)[1]
        known = state.get("mutable_fields", {}).get(field)
        if known is not None and known != claim["object_or_value"]:
            return {
                "outcome": "contradicted",
                "known_value": known,
                "correction_action": "block_claim_restore_last_valid_revision",
            }
        if known == claim["object_or_value"]:
            return {"outcome": "confirmed", "known_value": known}
    return {
        "outcome": "unknown_requires_retrieval",
        "correction_action": "verify_evidence_or_runtime_event",
    }


def commit_runtime_event(world_db, runtime_event, rule_decision):
    if rule_decision.get("decision") not in {
        "allowed", "allowed_with_consequence"
    }:
        raise ValueError("Runtime event cannot commit without an allowed rule decision.")
    state = json.loads(json.dumps(world_db["world_state"], ensure_ascii=False))
    before_revision = state["state_revision"]
    patches = []
    for change in runtime_event.get("state_changes", []):
        subject_id = change.get("subject_id")
        if subject_id not in state["entity_states"]:
            raise ValueError(f"Unknown state subject: {subject_id}")
        field = clean_graph_text(change.get("field", ""))
        if not field:
            raise ValueError("Every state change requires a field.")
        entity_state = state["entity_states"][subject_id]
        previous = entity_state["mutable_fields"].get(field, "unknown")
        expected_before = change.get("before", "unknown")
        if expected_before not in {"unknown", previous}:
            raise ValueError(
                f"Precondition failed for {subject_id}.{field}: {previous}"
            )
        entity_state["mutable_fields"][field] = change.get("after")
        entity_state["last_updated_by_event_id"] = runtime_event["event_id"]
        patches.append({
            "subject_id": subject_id,
            "field": field,
            "before": previous,
            "after": change.get("after"),
        })
    state["state_revision"] += 1
    state["runtime_event_ids"].append(runtime_event["event_id"])
    state["state_change_log"].append({
        "event_id": runtime_event["event_id"],
        "revision_before": before_revision,
        "revision_after": state["state_revision"],
        "patches": patches,
        "rule_ids": rule_decision.get("applicable_rules", []),
    })
    return state


