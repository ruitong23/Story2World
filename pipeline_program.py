def step15_organization_trust(record, entity_types_by_surface):
    structural = bool(
        record.get("members") or record.get("parent_organizations")
        or record.get("governed_entities") or record.get("governed_by")
        or record.get("authorized_actions")
    )
    if not structural:
        return False, "no_structural_relation"
    normalized_name = normalize_surface_name(record["canonical_name"])
    conflicting_types = (
        entity_types_by_surface.get(normalized_name, set())
        - {"Organization"}
    )
    if conflicting_types & {
        "Character", "TitleOrIdentity", "Location", "Event", "Goal",
        "Artifact", "Ability", "WorldRule", "KnowledgeScope",
    }:
        return False, "cross_type_name_conflict"
    attributes = record.get("attributes", {})
    mention_kinds = set(attributes.get("mention_kind", []))
    subtypes = {
        clean_graph_text(value).casefold()
        for value in attributes.get("organization_subtype", [])
    }
    if "named_entity" in mention_kinds:
        return True, "named_structured_organization"
    collective_subtypes = {
        "team", "crew", "military_force", "administrative_body",
        "alliance", "faction", "court", "company",
    }
    if (
        "collective" in mention_kinds
        and subtypes & collective_subtypes
        and (
            len(record.get("members", [])) >= 2
            or record.get("parent_organizations")
            or record.get("governed_entities")
            or record.get("governed_by")
        )
    ):
        return True, "structured_collective_organization"
    return False, "weak_organization_classification"


