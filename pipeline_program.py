def step15_semantic_links(entity_id, relation_ids, relation_by_id, entity_by_id):
    links = []
    for relation_id in relation_ids:
        relation = relation_by_id[relation_id]
        outgoing = relation["source_entity_id"] == entity_id
        other_id = (
            relation["target_entity_id"]
            if outgoing else relation["source_entity_id"]
        )
        other = entity_by_id[other_id]
        links.append({
            "relation_id": relation_id,
            "relation_type": relation["type"],
            "direction": "outgoing" if outgoing else "incoming",
            "other_entity_id": other_id,
            "other_entity_type": other["entity_type"],
            "other_name": other["canonical_name"],
            "mention_count": relation.get("mention_count", 0),
            "evidence": relation.get("evidence", []),
        })
    return sorted(links, key=lambda item: (
        item["relation_type"], item["direction"], item["other_name"]
    ))


