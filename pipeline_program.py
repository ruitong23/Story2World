    state["completed_chunk_count"] = len(state["results"])
    state["complete"] = completed_ids == set(expected_ids)
    state["quality_summary"] = {
        "valid_chunk_count": sum(
            item.get("validation_status") == "valid"
            for item in state["results"]
        ),
        "partial_chunk_count": sum(
            item.get("validation_status") == "partial"
            for item in state["results"]
        ),
        "validation_error_count": sum(
            len(item.get("validation_errors", []))
            for item in state["results"]
        ),
        "node_count": sum(len(item.get("nodes", [])) for item in state["results"]),
        "edge_count": sum(len(item.get("edges", [])) for item in state["results"]),
    }
    return state


