def deduplicate_results(
    results,
    similarity_threshold=0.8
):
    filtered = []
    seen_chunk_ids = set()

    for result in results:

        chunk_id = result["chunk_id"]

        if (
            chunk_id in seen_chunk_ids
            or chunk_id - 1 in seen_chunk_ids
            or chunk_id + 1 in seen_chunk_ids
        ):
            continue

        filtered.append(result)
        seen_chunk_ids.add(chunk_id)

    return filtered


