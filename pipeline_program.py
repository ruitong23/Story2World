# Step 6：Hybrid Search
# 目标：
# 1. 分别执行 dense_search 和 bm25_search
# 2. 按 chunk_idx 合并结果
# 3. 保留 vector_score、bm25_score、hybrid_score
# 4. 让语义检索和关键词检索互补


def normalize_scores(score_dict):
    """
    对分数做 min-max normalization
    输入格式：
    {
        chunk_idx: score,
        chunk_idx: score
    }
    """
    if not score_dict:
        return {}

    values = list(score_dict.values())
    min_score = min(values)
    max_score = max(values)

    normalized = {}

    for idx, score in score_dict.items():
        if max_score == min_score:
            normalized[idx] = 1.0
        else:
            normalized[idx] = (score - min_score) / (max_score - min_score)

    return normalized


def hybrid_search(
    query,
    chunks,
    embeddings,
    embedding_model,
    bm25_index,
    dense_top_k=10,
    bm25_top_k=10,
    final_top_k=5,
    vector_weight=0.4,
    bm25_weight=0.6
):
    """
    Dense + BM25 混合检索
    """
    dense_results = dense_search(
        query=query,
        chunks=chunks,
        embeddings=embeddings,
        embedding_model=embedding_model,
        top_k=dense_top_k
    )

    bm25_results = bm25_search(
        query=query,
        chunks=chunks,
        bm25_index=bm25_index,
        top_k=bm25_top_k
    )

    vector_scores = {
        result["chunk_idx"]: result["vector_score"]
        for result in dense_results
    }

    bm25_scores = {
        result["chunk_idx"]: result["bm25_score"]
        for result in bm25_results
    }

    normalized_vector_scores = normalize_scores(vector_scores)
    normalized_bm25_scores = normalize_scores(bm25_scores)

    all_chunk_indices = set(vector_scores.keys()) | set(bm25_scores.keys())

    merged_results = []

    for idx in all_chunk_indices:
        chunk = chunks[idx]

        vector_score = vector_scores.get(idx, 0.0)
        bm25_score = bm25_scores.get(idx, 0.0)

        normalized_vector_score = normalized_vector_scores.get(idx, 0.0)
        normalized_bm25_score = normalized_bm25_scores.get(idx, 0.0)

        hybrid_score = (
            vector_weight * normalized_vector_score
            + bm25_weight * normalized_bm25_score
        )

        merged_results.append({
            "chunk_idx": int(idx),
            "chunk_id": chunk["chunk_id"],
            "section_id": chunk["section_id"],
            "section_title": chunk["section_title"],
            "local_chunk_id": chunk["local_chunk_id"],
            "vector_score": float(vector_score),
            "bm25_score": float(bm25_score),
            "normalized_vector_score": float(normalized_vector_score),
            "normalized_bm25_score": float(normalized_bm25_score),
            "hybrid_score": float(hybrid_score),
            "text": chunk["text"]
        })

    merged_results = sorted(
        merged_results,
        key=lambda x: x["hybrid_score"],
        reverse=True
    )

    final_results = merged_results[:final_top_k]

    for rank, result in enumerate(final_results, start=1):
        result["rank"] = rank

    return final_results


