import jieba
from rank_bm25 import BM25Okapi


def tokenize_chinese_text(text):
    """
    中文分词函数
    """
    tokens = jieba.lcut(text)

    tokens = [
        token.strip()
        for token in tokens
        if token.strip()
    ]

    return tokens


def build_bm25_index(chunks):
    """
    给所有 chunk 建立 BM25 索引
    """
    tokenized_corpus = []

    for chunk in chunks:
        tokens = tokenize_chinese_text(chunk["text"])
        tokenized_corpus.append(tokens)

    bm25_index = BM25Okapi(tokenized_corpus)

    print("BM25 索引建立完成")
    print("chunk 数量:", len(chunks))
    print("分词后文档数量:", len(tokenized_corpus))

    return bm25_index, tokenized_corpus


def bm25_search(query, chunks, bm25_index, top_k=5):
    """
    BM25 稀疏检索
    """
    query_tokens = tokenize_chinese_text(query)

    scores = bm25_index.get_scores(query_tokens)

    top_indices = sorted(
        range(len(scores)),
        key=lambda i: scores[i],
        reverse=True
    )[:top_k]

    results = []

    for rank, idx in enumerate(top_indices, start=1):
        chunk = chunks[idx]

        results.append({
            "rank": rank,
            "chunk_idx": int(idx),
            "chunk_id": chunk["chunk_id"],
            "section_id": chunk["section_id"],
            "section_title": chunk["section_title"],
            "local_chunk_id": chunk["local_chunk_id"],
            "bm25_score": float(scores[idx]),
            "text": chunk["text"]
        })

    return results


