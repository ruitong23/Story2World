    raw = call_llm(
        STEP11_CATALOG_DISCOVERY_SYSTEM_PROMPT,
        prompt,
        temperature=0.0,
        max_tokens=2600,
    )
    payload = extract_json_from_llm_text(raw)
    if not isinstance(payload, dict):
        raise ValueError("Step 11 catalog discovery must return an object.")
    known_ids = set(cluster_summaries)
