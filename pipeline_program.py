def step16_unique(values):
    result = []
    seen = set()
    for value in values:
        marker = (
            stable_json_hash(value)
            if isinstance(value, (dict, list))
            else clean_graph_text(value)
        )
        if not marker or marker in seen:
            continue
        seen.add(marker)
        result.append(value)
    return result


def step16_relation_confidence(relation, canonical_character_ids):
    relation_type = relation.get("relation_type", "")
    evidence_count = len(relation.get("evidence", []))
    target_is_reference = (
        relation.get("entity_id") not in canonical_character_ids
        and relation.get("entity_type") == "Character"
    )
    if (
        relation_type == "HAS_RELATIONSHIP"
        or target_is_reference
        or evidence_count == 0
    ):
        return "low", 0.45
    if evidence_count >= 2:
        return "high", 0.9
    return "medium", 0.7


def step16_relation_record(
    relation,
    canonical_character_ids,
    self_name,
):
    label, score = step16_relation_confidence(
        relation, canonical_character_ids
    )
    evidence = []
    for item in relation.get("evidence", [])[:4]:
        source_text = clean_graph_text(item.get("source_text", ""))
        if not source_text:
            continue
        evidence.append({
            "mention_edge_id": item.get("mention_edge_id", ""),
            "source_chunk_id": item.get("source_chunk_id"),
            "source_text": source_text,
        })
    direction = relation.get("direction", "")
    other_name = relation.get("name", "")
    if direction == "outgoing":
        source_name, target_name = self_name, other_name
    else:
        source_name, target_name = other_name, self_name
    return {
        "relation_id": relation.get("relation_id", ""),
        "relation_type": relation.get("relation_type", ""),
        "source_relation_types": relation.get(
            "source_relation_types",
            [relation.get("relation_type", "")],
        ),
        "direction": direction,
        "entity_id": relation.get("entity_id", ""),
        "entity_type": relation.get("entity_type", ""),
        "name": other_name,
        "confidence": label,
        "confidence_score": score,
        "evidence": evidence,
        "edge_statement": (
            f"{source_name} --{relation.get('relation_type', '')}--> "
            f"{target_name}"
        ),
    }


def step16_merge_relations(
    relations,
    canonical_character_ids,
    self_name,
):
    grouped = {}
    for relation in relations:
        record = step16_relation_record(
            relation,
            canonical_character_ids,
            self_name,
        )
        key = (
            record["relation_type"],
            record["direction"],
            record["entity_id"],
        )
        if key not in grouped:
            grouped[key] = {
                **record,
                "relation_ids": [record["relation_id"]],
            }
            continue
        existing = grouped[key]
        existing["relation_ids"] = step16_unique(
            existing["relation_ids"] + [record["relation_id"]]
        )
        existing["source_relation_types"] = step16_unique(
            existing["source_relation_types"]
            + record["source_relation_types"]
        )
        existing["evidence"] = step16_unique(
            existing["evidence"] + record["evidence"]
        )
        if (
            len(existing["evidence"]) >= 2
            and existing["relation_type"] != "HAS_RELATIONSHIP"
            and existing["confidence"] != "low"
        ):
            existing["confidence"] = "high"
            existing["confidence_score"] = 0.9
    return list(grouped.values())


def step16_world_context(character, relations, world_db, limit):
    knowledge_by_id = {
        item["knowledge_id"]: item
        for item in world_db.get("knowledge_units", [])
    }
    references = defaultdict(set)
    for item in world_db.get(
        "character_world_refs", {}
    ).get(character["character_id"], []):
        references[item["knowledge_id"]].add("direct")

    relation_ids = {
        relation.get("relation_id")
        for relation in relations
        if relation.get("relation_id")
    }
    for unit in world_db.get("knowledge_units", []):
        if unit.get("model_status", "trusted") not in {
            "trusted", "supported"
        }:
            continue
        if relation_ids & set(unit.get("relation_ids", [])):
            references[unit["knowledge_id"]].add(
                "relation_fallback"
            )

    units = []
    supported_units = []
    for knowledge_id, sources in references.items():
        unit = knowledge_by_id.get(knowledge_id)
        if not unit:
            continue
        status = unit.get("model_status", "unresolved")
        if status not in {"trusted", "supported"}:
            continue
        record = {
            "knowledge_id": unit["knowledge_id"],
            "concept_id": unit["entity_id"],
            "entity_id": unit["entity_id"],
            "category": unit["category"],
            "name": unit["name"],
            "model_status": status,
            "descriptions": step16_unique(
                unit.get("descriptions", [])
            ),
            "reference_sources": sorted(sources),
            "source_chunk_ids": unit.get("source_chunk_ids", []),
            "retrieval_tags": unit.get("retrieval_tags", []),
            "language_status": "cleaned_in_step15",
            "runtime_use": (
                "direct_context"
                if status == "trusted"
                else "on_demand_with_uncertainty"
            ),
        }
        (units if status == "trusted" else supported_units).append(record)
    units.sort(key=lambda item: (
        "direct" not in item["reference_sources"],
        item["category"],
        item["name"],
    ))
    supported_units.sort(key=lambda item: (
        "direct" not in item["reference_sources"],
        item["category"],
        item["name"],
    ))
    lookup_surfaces = {
        normalize_surface_name(value)
        for value in [
            character["canonical_name"],
            *character.get("aliases", []),
            *(relation.get("name", "") for relation in relations),
        ]
        if normalize_surface_name(value)
    }
    concept_routes = [
        {
            "surface": surface,
            "default_concept_id": record.get("default_concept_id"),
            "requires_intent": record.get("requires_intent", False),
            "resolution_policy": record.get("resolution_policy", ""),
            "intent_routes": record.get("intent_routes", {}),
        }
        for surface, record in world_db.get("concept_registry", {}).items()
        if surface in lookup_surfaces
    ]
    applicable_rule_ids = [
        rule["rule_id"]
        for rule in world_db.get("rule_engine", {}).get("rules", [])
        if character["character_id"] in set(
            rule.get("applies_to", []) + rule.get("constrains", [])
        )
    ]
    sections = world_db.get("world_sections", {})
    missing_world_rules = {
        key: not bool(sections.get(key))
        for key in (
            "organizations",
            "events",
            "world_rules",
            "knowledge_scopes",
        )
    }
    return {
        "knowledge_refs": units[:limit],
        "supported_retrieval_candidates": supported_units[:limit],
        "concept_routes": concept_routes[:limit],
        "world_state_revision": world_db.get(
            "world_state", {}
        ).get("state_revision", 0),
        "entity_state": world_db.get(
            "world_state", {}
        ).get("entity_states", {}).get(character["character_id"]),
        "applicable_rule_ids": applicable_rule_ids,
        "rule_decision_contract": world_db.get(
            "rule_engine", {}
        ).get("decision_contract", {}),
        "model_status_policy": world_db.get(
            "runtime_contracts", {}
        ).get("model_status_policy", {}),
        "missing_world_rules": missing_world_rules,
        "fallback_used": any(
            "relation_fallback" in item["reference_sources"]
            for item in units
        ),
        "unresolved_and_rejected_direct_injection": False,
    }


def step16_memories(character, relation_ids, world_db, limit):
    records = []
    for item in world_db.get("timeline", []):
        if item.get("relation_id") not in relation_ids:
            continue
        if item.get("relation_type") in STEP16_STATIC_TIMELINE_TYPES:
            continue
        if character["character_id"] not in {
            item.get("source_entity_id"),
            item.get("target_entity_id"),
        }:
            continue
        source_text = clean_graph_text(item.get("source_text", ""))
        summary = clean_graph_text(item.get("relation_summary", ""))
        if not source_text and not summary:
            continue
        records.append({
            "timeline_id": item["timeline_id"],
            "entry_kind": "relation_occurrence",
            "relation_id": item["relation_id"],
            "relation_type": item["relation_type"],
            "source_name": item.get("source_name", ""),
            "target_name": item.get("target_name", ""),
            "source_chunk_ids": item.get("source_chunk_ids", []),
            "source_text": source_text,
            "relation_summary": summary,
            "memory_scope": (
                "evidence_reference_not_complete_event"
            ),
        })
    records.sort(key=lambda item: (
        item.get("source_chunk_ids", [""])[0]
        if item.get("source_chunk_ids") else "",
        item["timeline_id"],
    ))
    return records[:limit]


