from sentence_transformers import SentenceTransformer
import numpy as np


def load_embedding_model(model_name="BAAI/bge-small-zh-v1.5"):
    """
    加载 embedding 模型
    """
    model = SentenceTransformer(model_name)
    print("Embedding 模型加载完成:", model_name)
    return model


def build_chunk_embeddings(chunks, embedding_model, batch_size=32):
    """
    给所有 chunk 生成 embedding
    """
    texts = [chunk["text"] for chunk in chunks]

    embeddings = embedding_model.encode(
        texts,
        batch_size=batch_size,
        show_progress_bar=True,
        normalize_embeddings=True
    )

    embeddings = np.array(embeddings)

    print("chunk 数量:", len(chunks))
    print("embedding shape:", embeddings.shape)

    return embeddings


def dense_search(query, chunks, embeddings, embedding_model, top_k=5):
    """
    Dense 向量检索
    使用 cosine similarity
    因为 embedding 已经 normalize，所以点积就是 cosine similarity
    """
    query_embedding = embedding_model.encode(
        query,
        normalize_embeddings=True
    )

    scores = np.dot(embeddings, query_embedding)

    top_indices = np.argsort(scores)[::-1][:top_k]

    results = []

    for rank, idx in enumerate(top_indices, start=1):
        chunk = chunks[idx]

        results.append({
            "rank": rank,
            "chunk_idx": int(idx),
            "chunk_id": chunk["chunk_id"],
            "section_id": chunk["section_id"],
            "section_title": chunk["section_title"],
            "local_chunk_id": chunk["local_chunk_id"],
            "vector_score": float(scores[idx]),
            "text": chunk["text"]
        })

    return results


