def filter_step9_authority_result(result, primary_result):
    forbidden_names = {
        normalized_evidence_text(node["surface_name"])
        for node in primary_result.get("nodes", [])
        if node["type"] in {"Character", "Location", "Artifact", "Ability"}
    }
    candidate_organizations = {
        node["node_id"] for node in result.get("nodes", [])
        if node["type"] == "Organization"
        and normalized_evidence_text(node["surface_name"]) not in forbidden_names
        and node.get("attributes", {}).get("organization_subtype")
        and node.get("attributes", {}).get("mention_kind") in {
            "named_entity", "collective"
        }
    }
    kept_edges = [
        edge for edge in result.get("edges", [])
        if edge["type"] in STEP9_AUTHORITY_EDGE_TYPES
        and (
            edge["source"] in candidate_organizations
            or edge["target"] in candidate_organizations
        )
    ]
    connected_organizations = {
        endpoint
        for edge in kept_edges
        for endpoint in (edge["source"], edge["target"])
        if endpoint in candidate_organizations
    }
    kept_edges = [
        edge for edge in kept_edges
        if not (
            edge["source"] in candidate_organizations
            and edge["source"] not in connected_organizations
        )
        and not (
            edge["target"] in candidate_organizations
            and edge["target"] not in connected_organizations
        )
    ]
    endpoint_ids = {
        endpoint for edge in kept_edges
        for endpoint in (edge["source"], edge["target"])
    }
    filtered = dict(result)
    filtered["nodes"] = [
        node for node in result.get("nodes", [])
        if node["node_id"] in endpoint_ids
        and (
            node["type"] != "Organization"
            or node["node_id"] in connected_organizations
        )
    ]
    kept_node_ids = {node["node_id"] for node in filtered["nodes"]}
    filtered["edges"] = [
        edge for edge in kept_edges
        if edge["source"] in kept_node_ids and edge["target"] in kept_node_ids
    ]
    filtered["validation_summary"] = {
        **result.get("validation_summary", {}),
        "accepted_organization_count": len(connected_organizations),
        "accepted_authority_edge_count": len(filtered["edges"]),
    }
    return filtered



