def recover_llm_text_from_exception(error):
    """
    从本地 LLM API 的 400 error 里恢复已经生成出来的文本。
    只要 error string 里出现 【NODES】，就从那里截出来。
    """

    error_text = str(error)

    start = error_text.find("【NODES】")
    if start == -1:
        return None

    recovered = error_text[start:]

    # 去掉 Python dict / quote 残留
    for tail in ["'}", '"}', "')", '")']:
        if recovered.endswith(tail):
            recovered = recovered[:-len(tail)]

    recovered = recovered.strip().strip("'").strip('"').strip()

    # 如果里面是字面量 \n，转成真实换行
    if "\\n" in recovered and "\n" not in recovered[:200]:
        recovered = recovered.replace("\\n", "\n")

    return recovered


