def normalize_step9_payload(payload, chunk, chunk_index, ontology):
    if not isinstance(payload, dict):
        raise ValueError("Step 9 response must be a JSON object.")

    chunk_id = get_chunk_id(chunk, chunk_index)
    chunk_text = get_chunk_text(chunk)
    evidence_haystack = normalized_evidence_text(chunk_text)
    entity_types, optional_fields, edge_types = build_step9_constraints(ontology)
    errors = []
    warnings = []
    nodes = []
    node_ref_to_node = {}

    raw_nodes = payload.get("nodes", [])
    raw_edges = payload.get("edges", [])
    raw_uncertain = payload.get("uncertain", [])
    if not isinstance(raw_nodes, list) or not isinstance(raw_edges, list):
        raise ValueError("nodes and edges must be JSON arrays.")

    for position, raw_node in enumerate(raw_nodes, start=1):
        if not isinstance(raw_node, dict):
            errors.append(f"node {position}: not an object")
            continue

        node_ref = clean_graph_text(raw_node.get("node_ref") or f"n{position}")
        node_type = clean_graph_text(raw_node.get("type"))
        surface_name = clean_graph_text(raw_node.get("surface_name"))
        source_text = clean_graph_text(raw_node.get("source_text"))
        description = clean_graph_text(raw_node.get("description"))

        if not node_ref or node_ref in node_ref_to_node:
            errors.append(f"node {position}: missing or duplicate node_ref")
            continue
        if node_type not in entity_types:
            errors.append(f"node {node_ref}: disallowed type {node_type!r}")
            continue
        if not surface_name:
            errors.append(f"node {node_ref}: missing surface_name")
            continue
        if normalized_evidence_text(surface_name) not in normalized_evidence_text(source_text):
            errors.append(f"node {node_ref}: surface_name is not present in source_text")
            continue
        if (
            node_type not in {
                "Event",
                "WorldRule",
                "Goal",
                "Memory",
                "KnowledgeScope",
                "SpeechStyle",
            }
            and re.search(r"[，,。；;！？!?、]", surface_name)
        ):
            errors.append(f"node {node_ref}: surface_name is a proposition or name list")
            continue
        if not source_text or normalized_evidence_text(source_text) not in evidence_haystack:
            errors.append(f"node {node_ref}: source_text is not an exact chunk excerpt")
            continue

        raw_attributes = dict(raw_node.get("attributes") or {})
        if raw_node.get("mention_kind"):
            raw_attributes["mention_kind"] = raw_node.get("mention_kind")
        if not isinstance(raw_attributes, dict):
            raw_attributes = {}
            warnings.append(f"node {node_ref}: non-object attributes removed")
        attributes = {
            key: clean_graph_text(value)
            for key, value in raw_attributes.items()
            if key in optional_fields[node_type] and clean_graph_text(value)
        }
        allowed_mention_kinds = {
            "named_entity", "generic_role", "collective",
            "temporary_identity", "descriptive_reference",
        }
        mention_kind = attributes.get("mention_kind", "")
        if mention_kind not in allowed_mention_kinds:
            mention_kind = (
                "temporary_identity"
                if node_type == "TitleOrIdentity"
                else "descriptive_reference"
            )
            attributes["mention_kind"] = mention_kind

        safe_chunk_id = re.sub(r"[^0-9A-Za-z_.-]+", "_", str(chunk_id))
        safe_ref = re.sub(r"[^0-9A-Za-z_.-]+", "_", node_ref)
        node = {
            "node_id": f"chunk_{safe_chunk_id}_{safe_ref}",
            "node_ref": node_ref,
            "type": node_type,
            "surface_name": surface_name,
            "description": description,
            "attributes": attributes,
            "source_chunk_id": chunk_id,
            "source_text": source_text,
        }
        nodes.append(node)
        node_ref_to_node[node_ref] = node

    edges = []
    seen_edge_keys = set()
    for position, raw_edge in enumerate(raw_edges, start=1):
        if not isinstance(raw_edge, dict):
            errors.append(f"edge {position}: not an object")
            continue

        edge_ref = clean_graph_text(raw_edge.get("edge_ref") or f"e{position}")
        edge_type = clean_graph_text(raw_edge.get("type"))
        source_ref = clean_graph_text(raw_edge.get("source_node"))
        target_ref = clean_graph_text(raw_edge.get("target_node"))
        source_text = clean_graph_text(raw_edge.get("source_text"))
        relation_summary = clean_graph_text(raw_edge.get("relation_summary"))

        if edge_type not in edge_types:
            errors.append(f"edge {edge_ref}: disallowed type {edge_type!r}")
            continue
        if source_ref not in node_ref_to_node or target_ref not in node_ref_to_node:
            errors.append(f"edge {edge_ref}: endpoint ref not found")
            continue
        if source_ref == target_ref:
            errors.append(f"edge {edge_ref}: self-loop")
            continue
        if not source_text or normalized_evidence_text(source_text) not in evidence_haystack:
            errors.append(f"edge {edge_ref}: source_text is not an exact chunk excerpt")
            continue

        source_node = node_ref_to_node[source_ref]
        target_node = node_ref_to_node[target_ref]
        edge_type = refine_extracted_relation_type(
            edge_type,
            source_node,
            target_node,
            source_text,
            relation_summary,
        )
        if edge_type in {
            "TRANSFORMS_INTO", "IMPERSONATES", "REVEALED_AS"
        }:
            target_surface = normalized_evidence_text(
                target_node["surface_name"]
            )
            if (
                target_surface
                and target_surface
                not in normalized_evidence_text(source_text)
            ):
                errors.append(
                    f"edge {edge_ref}: identity/form target is absent "
                    "from source_text"
                )
                continue
        if edge_type not in edge_types:
            errors.append(
                f"edge {edge_ref}: refined to unavailable type {edge_type!r}"
            )
            continue
        rule = edge_types[edge_type]
        if rule["source_types"] and source_node["type"] not in rule["source_types"]:
            errors.append(
                f"edge {edge_ref}: {source_node['type']} cannot source {edge_type}"
            )
            continue
        if rule["target_types"] and target_node["type"] not in rule["target_types"]:
            errors.append(
                f"edge {edge_ref}: {target_node['type']} cannot target {edge_type}"
            )
            continue

        edge_key = (edge_type, source_node["node_id"], target_node["node_id"], source_text)
        if edge_key in seen_edge_keys:
            warnings.append(f"edge {edge_ref}: duplicate removed")
            continue
        seen_edge_keys.add(edge_key)

        safe_chunk_id = re.sub(r"[^0-9A-Za-z_.-]+", "_", str(chunk_id))
        safe_ref = re.sub(r"[^0-9A-Za-z_.-]+", "_", edge_ref)
        raw_edge_attributes = raw_edge.get("attributes") or {}
        if not isinstance(raw_edge_attributes, dict):
            raw_edge_attributes = {}
        address_term = clean_graph_text(
            raw_edge.get("address_term")
            or raw_edge_attributes.get("address_term")
        )
        if (
            edge_type == "ADDRESSES_AS"
            and address_term
            and normalized_evidence_text(address_term)
                not in normalized_evidence_text(source_text)
        ):
            warnings.append(
                f"edge {edge_ref}: unsupported address_term removed"
            )
            address_term = ""
        edges.append({
            "edge_id": f"chunk_{safe_chunk_id}_{safe_ref}",
            "edge_ref": edge_ref,
            "type": edge_type,
            "source": source_node["node_id"],
            "target": target_node["node_id"],
            "source_surface_name": source_node["surface_name"],
            "target_surface_name": target_node["surface_name"],
            "relation_summary": relation_summary,
            "source_chunk_id": chunk_id,
            "source_text": source_text,
            "relation_attributes": (
                {"address_term": address_term}
                if address_term else {}
            ),
        })

    uncertain = []
    if isinstance(raw_uncertain, list):
        for item in raw_uncertain:
            if not isinstance(item, dict):
                continue
            text = clean_graph_text(item.get("text"))
            reason = clean_graph_text(item.get("reason"))
            if text and normalized_evidence_text(text) in evidence_haystack:
                uncertain.append({"text": text, "reason": reason})

    result = {
        "chunk_id": chunk_id,
        "chunk_index": chunk_index,
        "chunk_text_hash": hashlib.sha256(
            chunk_text.encode("utf-8")
        ).hexdigest(),
        "nodes": nodes,
        "edges": edges,
        "uncertain": uncertain,
        "validation_summary": {
            "input_node_count": len(raw_nodes),
            "node_count": len(nodes),
            "input_edge_count": len(raw_edges),
            "edge_count": len(edges),
            "uncertain_count": len(uncertain),
            "warning_count": len(warnings),
        },
        "validation_warnings": warnings,
    }
    return result, errors



def extract_complete_objects_from_array(text, key):
    match = re.search(rf'"{re.escape(key)}"\s*:\s*\[', text)
    if not match:
        return []
    objects = []
    index = match.end()
    while index < len(text):
        while index < len(text) and text[index] in " \t\r\n,":
            index += 1
        if index >= len(text) or text[index] == "]":
            break
        if text[index] != "{":
            index += 1
            continue

        start = index
        depth = 0
        in_string = False
        escaped = False
        while index < len(text):
            character = text[index]
            if in_string:
                if escaped:
                    escaped = False
                elif character == "\\":
                    escaped = True
                elif character == '"':
                    in_string = False
            else:
                if character == '"':
                    in_string = True
                elif character == "{":
                    depth += 1
                elif character == "}":
                    depth -= 1
                    if depth == 0:
                        candidate = text[start:index + 1]
                        try:
                            objects.append(json.loads(candidate))
                        except json.JSONDecodeError:
                            pass
                        index += 1
                        break
            index += 1
        else:
            break
    return objects


def extract_step9_payload(raw_text):
    try:
        return extract_json_from_llm_text(raw_text)
    except Exception:
        recovered = {
            "nodes": extract_complete_objects_from_array(raw_text, "nodes"),
            "edges": extract_complete_objects_from_array(raw_text, "edges"),
            "uncertain": extract_complete_objects_from_array(raw_text, "uncertain"),
        }
        if recovered["nodes"] or recovered["edges"]:
            return recovered
        raise


def merge_step9_results(primary, world, secondary_label="world-pass"):
    merged = dict(primary)
    reusable_types = {"Character", "Location", "Artifact", "Ability"}
    primary_lookup = {
        (node["type"], normalized_evidence_text(node["surface_name"])): node
        for node in primary.get("nodes", [])
        if node["type"] in reusable_types
    }
    endpoint_remap = {}
    secondary_nodes = []
    for node in world.get("nodes", []):
        key = (node["type"], normalized_evidence_text(node["surface_name"]))
        existing = primary_lookup.get(key)
        if existing is not None and node["type"] in reusable_types:
            endpoint_remap[node["node_id"]] = existing["node_id"]
            continue
        secondary_nodes.append(node)
    secondary_edges = []
    seen_edges = {
        (edge["type"], edge["source"], edge["target"], edge["source_text"])
        for edge in primary.get("edges", [])
    }
    node_by_id = {
        node["node_id"]: node
        for node in primary.get("nodes", []) + secondary_nodes
    }
    for raw_edge in world.get("edges", []):
        edge = dict(raw_edge)
        edge["source"] = endpoint_remap.get(edge["source"], edge["source"])
        edge["target"] = endpoint_remap.get(edge["target"], edge["target"])
        if edge["source"] == edge["target"]:
            continue
        source_node = node_by_id.get(edge["source"])
        target_node = node_by_id.get(edge["target"])
        if source_node:
            edge["source_surface_name"] = source_node["surface_name"]
        if target_node:
            edge["target_surface_name"] = target_node["surface_name"]
        edge_key = (
            edge["type"], edge["source"], edge["target"], edge["source_text"]
        )
        if edge_key in seen_edges:
            continue
        seen_edges.add(edge_key)
        secondary_edges.append(edge)
    merged["nodes"] = primary.get("nodes", []) + secondary_nodes
    merged["edges"] = primary.get("edges", []) + secondary_edges
    merged["uncertain"] = (
        primary.get("uncertain", []) + world.get("uncertain", [])
    )
    primary_errors = [
        item if re.match(r"^[a-z-]+-pass: ", item)
        else "character-pass: " + item
        for item in primary.get("validation_errors", [])
    ]
    world_errors = [
        secondary_label + ": " + item
        for item in world.get("validation_errors", [])
    ]
    merged["validation_errors"] = primary_errors + world_errors
    merged["validation_status"] = (
        "valid" if not merged["validation_errors"] else "partial"
    )
    merged["validation_summary"] = {
        "node_count": len(merged["nodes"]),
        "edge_count": len(merged["edges"]),
        "uncertain_count": len(merged["uncertain"]),
        "character_pass": primary.get("validation_summary", {}),
        "world_pass": world.get("validation_summary", {}),
    }
    merged["extraction_attempts"] = {
        "previous": primary.get("extraction_attempts", 1),
        secondary_label.replace("-", "_"): world.get("extraction_attempts", 1),
    }
    return merged


def extract_step9_pass(
    chunk, chunk_index, ontology, prompt, system_prompt,
    max_tokens, validation_retries, ref_prefix="",
):
    feedback = ""
    last_error = None
    best_result = None
    best_score = None
    for attempt in range(validation_retries + 1):
        attempt_prompt = prompt
        if feedback:
            attempt_prompt += (
                "\n\nYour previous response failed validation. "
                "Return a shorter corrected JSON object and fix:\n" + feedback
            )
        raw_text = call_llm(
            system_prompt=system_prompt,
            user_prompt=attempt_prompt,
            temperature=0.0,
            max_tokens=max_tokens,
        )
        try:
            payload = extract_step9_payload(raw_text)
            if ref_prefix:
                ref_map = {}
                for position, node in enumerate(payload.get("nodes", []), 1):
                    old_ref = clean_graph_text(
                        node.get("node_ref") or f"n{position}"
                    )
                    new_ref = ref_prefix + old_ref
                    ref_map[old_ref] = new_ref
                    node["node_ref"] = new_ref
                for position, edge in enumerate(payload.get("edges", []), 1):
                    old_edge_ref = clean_graph_text(
                        edge.get("edge_ref") or f"e{position}"
                    )
                    edge["edge_ref"] = ref_prefix + old_edge_ref
                    edge["source_node"] = ref_map.get(
                        clean_graph_text(edge.get("source_node")),
                        ref_prefix + clean_graph_text(edge.get("source_node")),
                    )
                    edge["target_node"] = ref_map.get(
                        clean_graph_text(edge.get("target_node")),
                        ref_prefix + clean_graph_text(edge.get("target_node")),
                    )
            result, errors = normalize_step9_payload(
                payload, chunk, chunk_index, ontology
            )
            score = (len(result["nodes"]) + 2 * len(result["edges"]), -len(errors))
            if best_score is None or score > best_score:
                best_result = result
                best_score = score
                best_result["validation_errors"] = errors
                best_result["extraction_attempts"] = attempt + 1
            if not errors:
                result["validation_errors"] = []
                result["validation_status"] = "valid"
                result["extraction_attempts"] = attempt + 1
                return result
            feedback = "\n".join(f"- {item}" for item in errors[:12])
            last_error = ValueError(feedback)
        except Exception as exc:
            last_error = exc
            feedback = f"- invalid JSON or schema: {exc}"
    if best_result is not None:
        best_result["validation_status"] = "partial"
        return best_result
    raise RuntimeError(f"Step 9 pass produced no parseable graph: {last_error}")


def extract_graph_from_chunk(chunk, chunk_index, ontology):
    prompt = build_step9_prompt(
        get_chunk_id(chunk, chunk_index),
        get_chunk_text(chunk),
        ontology,
    )
    primary = extract_step9_pass(
        chunk, chunk_index, ontology, prompt, STEP9_SYSTEM_PROMPT,
        STEP9_MAX_TOKENS, STEP9_VALIDATION_RETRIES,
    )
    if len(clean_graph_text(get_chunk_text(chunk))) < 80:
        return primary
    world_prompt = build_step9_world_prompt(
        get_chunk_id(chunk, chunk_index), get_chunk_text(chunk), ontology
    )
    world = extract_step9_pass(
        chunk, chunk_index, ontology, world_prompt, STEP9_WORLD_SYSTEM_PROMPT,
        STEP9_WORLD_MAX_TOKENS, STEP9_WORLD_VALIDATION_RETRIES, ref_prefix="w_",
    )
    merged = merge_step9_results(primary, world)
    # Authority is sparse and easy to miss in a general extraction. This
    # compact sub-pass runs once for each substantive chunk and may correctly
    # return empty arrays; semantic filtering keeps only structured results.
    authority_prompt = build_step9_authority_prompt(
        get_chunk_id(chunk, chunk_index), get_chunk_text(chunk),
        merged, ontology,
    )
    authority = extract_step9_pass(
        chunk, chunk_index, ontology, authority_prompt,
        STEP9_AUTHORITY_SYSTEM_PROMPT, STEP9_AUTHORITY_MAX_TOKENS,
        STEP9_AUTHORITY_VALIDATION_RETRIES, ref_prefix="a_",
    )
    authority = filter_step9_authority_result(authority, merged)
    merged = merge_step9_results(
        merged, authority, secondary_label="authority-pass"
    )
    return merged


