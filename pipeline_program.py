def text_matches_language(value, profile=None):
    text = str(value or "").strip()
    if not text:
        return False
    profile = profile or get_project_language_profile()
    expected = profile.get("dominant_script", "unknown")
    if expected == "unknown":
        return True
    actual = detect_text_language_profile(text)
    counts = actual["script_counts"]
    expected_count = counts.get(expected, 0)
    other_count = sum(counts.values()) - expected_count
    return expected_count > 0 and expected_count >= other_count


