def stable_unique_records(records, key_fields):
    seen = set()
    output = []
    for record in records:
        marker = tuple(record.get(field) for field in key_fields)
        if marker not in seen:
            seen.add(marker)
            output.append(record)
    return output


def build_structured_world_graph(normalized, canonical):
    expected_canonical_fingerprint = stable_json_hash({
        "canonical_entities": canonical["canonical_entities"],
        "reference_entities": canonical.get("reference_entities", []),
        "node_id_to_entity_id": canonical["node_id_to_entity_id"],
    })
    if normalized.get("source_canonical_fingerprint") != expected_canonical_fingerprint:
        raise ValueError("Normalized graph and canonical entity file do not match.")

    mention_nodes = defaultdict(list)
    edge_groups = defaultdict(list)
    for chunk_result in normalized["results"]:
        for node in chunk_result["nodes"]:
            mention_nodes[node["entity_id"]].append(node)
        for edge in chunk_result["edges"]:
            key = (
                edge["type"],
                edge["source_entity_id"],
                edge["target_entity_id"],
            )
            edge_groups[key].append(edge)

    entities = []
    all_resolved_entities = (
        canonical["canonical_entities"]
        + canonical.get("reference_entities", [])
    )
    for canonical_entity in all_resolved_entities:
        entity_id = canonical_entity["entity_id"]
        mentions = mention_nodes.get(entity_id, [])
        descriptions = sorted({
            item["description"]
            for item in mentions
            if item.get("description")
        })
        attributes = defaultdict(set)
        evidence = []
        for item in mentions:
            for key, value in item.get("attributes", {}).items():
                if value:
                    attributes[key].add(value)
            evidence.append({
                "source_chunk_id": item["source_chunk_id"],
                "surface_name": item["surface_name"],
                "source_text": item["source_text"],
            })
        entities.append({
            **canonical_entity,
            "descriptions": descriptions,
            "attributes": {
                key: sorted(values)
                for key, values in sorted(attributes.items())
            },
            "evidence": stable_unique_records(
                evidence,
                ("source_chunk_id", "surface_name", "source_text"),
            ),
        })

    relations = []
    outgoing = defaultdict(list)
    incoming = defaultdict(list)
    for (edge_type, source_id, target_id), mentions in sorted(edge_groups.items()):
        relation_id = "relation_" + hashlib.sha1(
            f"{edge_type}|{source_id}|{target_id}".encode("utf-8")
        ).hexdigest()[:16]
        evidence = stable_unique_records(
            [
                {
                    "mention_edge_id": item["mention_edge_id"],
                    "source_chunk_id": item["source_chunk_id"],
                    "source_text": item["source_text"],
                    "relation_summary": item.get("relation_summary", ""),
                    "source_surface_name": item.get("source_surface_name", ""),
                    "target_surface_name": item.get("target_surface_name", ""),
                    "relation_attributes": item.get(
                        "relation_attributes", {}
                    ),
                }
                for item in mentions
            ],
            ("source_chunk_id", "source_text", "relation_summary"),
        )
        relation = {
            "relation_id": relation_id,
            "type": edge_type,
            "source_entity_id": source_id,
            "target_entity_id": target_id,
            "mention_count": len(mentions),
            "evidence": evidence,
        }
        relations.append(relation)
        outgoing[source_id].append(relation_id)
        incoming[target_id].append(relation_id)

    output = {
        "schema_version": STEP13_SCHEMA_VERSION,
        "step": 13,
        "purpose": "Canonical structured world graph.",
        "source_normalized_graph_fingerprint": normalized["normalized_graph_fingerprint"],
        "entity_count": len(entities),
        "relation_count": len(relations),
        "identity_facts": normalized.get("identity_facts", []),
        "entities": entities,
        "relations": relations,
        "adjacency": {
            entity["entity_id"]: {
                "outgoing_relation_ids": sorted(outgoing[entity["entity_id"]]),
                "incoming_relation_ids": sorted(incoming[entity["entity_id"]]),
            }
            for entity in entities
        },
    }
    if any(item["source_entity_id"] == item["target_entity_id"] for item in relations):
        raise ValueError("Step 13 relation graph contains a self-loop.")
    output["world_graph_fingerprint"] = stable_json_hash({
        "entities": entities,
        "relations": relations,
        "identity_facts": output["identity_facts"],
    })
    return output


