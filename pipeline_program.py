def step9_result_matches_chunk(result, chunk, chunk_index):
    current_hash = hashlib.sha256(
        get_chunk_text(chunk).encode("utf-8")
    ).hexdigest()
    saved_hash = clean_graph_text(result.get("chunk_text_hash"))
    if saved_hash:
        return saved_hash == current_hash

    # Migration for checkpoints created before chunk hashes were stored.
    haystack = normalized_evidence_text(get_chunk_text(chunk))
    evidence = [
        clean_graph_text(item.get("source_text"))
        for group in (result.get("nodes", []), result.get("edges", []))
        for item in group
        if clean_graph_text(item.get("source_text"))
    ]
    evidence.extend(
        clean_graph_text(item.get("text"))
        for item in result.get("uncertain", [])
        if clean_graph_text(item.get("text"))
    )
    return bool(evidence) and all(
        normalized_evidence_text(item) in haystack
        for item in evidence
    )


def load_or_create_step9_state(chunks, ontology):
    expected = new_step9_state(chunks, ontology)
    if STEP9_FORCE_REBUILD or not STEP9_OUTPUT_PATH.exists():
        return expected

    existing = json.loads(STEP9_OUTPUT_PATH.read_text(encoding="utf-8"))
    if not isinstance(existing, dict) or existing.get("schema_version") != STEP9_SCHEMA_VERSION:
        if STEP9_RESET_LEGACY_OUTPUT:
            print("Legacy Step 9 output detected; rebuilding it with schema v2.")
            return expected
        raise ValueError("Legacy raw_graph_triples.json detected. Enable STEP9_RESET_LEGACY_OUTPUT.")

    if existing.get("ontology_fingerprint") != expected["ontology_fingerprint"]:
        print("Step 9 ontology changed; starting a new extraction checkpoint.")
        return expected

    current_source = expected["source_text_fingerprint"]
    saved_source = existing.get("source_text_fingerprint")
    if saved_source and saved_source != current_source:
        print("Step 9 source novel changed; starting a new extraction checkpoint.")
        return expected

    chunk_by_id = {
        str(get_chunk_id(chunk, index)): (chunk, index)
        for index, chunk in enumerate(chunks)
    }
    reusable = []
    for result in existing.get("results", []):
        pair = chunk_by_id.get(str(result.get("chunk_id")))
        if pair is None:
            continue
        chunk, chunk_index = pair
        if not step9_result_matches_chunk(result, chunk, chunk_index):
            continue
        migrated = dict(result)
        migrated["chunk_text_hash"] = hashlib.sha256(
            get_chunk_text(chunk).encode("utf-8")
        ).hexdigest()
        reusable.append(migrated)

    expected["results"] = reusable
    expected["completed_chunk_count"] = len(reusable)
    print(
        "Step 9 resume:",
        f"reusing {len(reusable)} completed chunks; "
        f"{len(chunks) - len(reusable)} remain for this scope.",
    )
    return expected


