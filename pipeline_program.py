import re


def split_by_chinese_chapters(text):
    """
    按中文章节标题切分
    例如：
    第一章 xxx
    第1章 xxx
    卷一 xxx
    楔子
    序章
    """
    chapter_pattern = re.compile(
        r'(?m)^\s*(第[一二三四五六七八九十百千万零〇\d]+[章回节卷部].*|卷[一二三四五六七八九十百千万零〇\d]+.*|序章.*|楔子.*|引子.*|后记.*)\s*$'
    )

    matches = list(chapter_pattern.finditer(text))

    if len(matches) < 2:
        return None

    sections = []

    # 处理正文开头没有标题的部分
    if matches[0].start() > 0:
        intro_content = text[:matches[0].start()].strip()
        if intro_content:
            sections.append({
                "title": "前言/未命名开头",
                "content": intro_content
            })

    for i, match in enumerate(matches):
        title = match.group().strip()
        start = match.end()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)

        content = text[start:end].strip()

        if content:
            sections.append({
                "title": title,
                "content": content
            })

    return sections


def split_by_english_chapters(text):
    """
    按英文 Chapter 切分
    例如：
    Chapter 1
    CHAPTER I
    Chapter One
    """
    chapter_pattern = re.compile(
        r'(?mi)^(chapter\s+[\divxlcdm]+.*|chapter\s+[a-z]+.*)$'
    )

    matches = list(chapter_pattern.finditer(text))

    if len(matches) < 2:
        return None

    sections = []

    if matches[0].start() > 0:
        intro_content = text[:matches[0].start()].strip()
        if intro_content:
            sections.append({
                "title": "Introduction / Untitled Start",
                "content": intro_content
            })

    for i, match in enumerate(matches):
        title = match.group().strip()
        start = match.end()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)

        content = text[start:end].strip()

        if content:
            sections.append({
                "title": title,
                "content": content
            })

    return sections


def split_by_paragraph_sections(text, max_chars=5000):
    """
    如果没有章节结构，就按段落聚合。
    每个 section 大约 max_chars 字。
    """
    paragraphs = [p.strip() for p in re.split(r'\n+', text) if p.strip()]

    sections = []
    current_content = []
    current_len = 0
    section_id = 1

    for paragraph in paragraphs:
        paragraph_len = len(paragraph)

        if current_len + paragraph_len > max_chars and current_content:
            sections.append({
                "title": f"Section {section_id}",
                "content": "\n".join(current_content)
            })

            section_id += 1
            current_content = []
            current_len = 0

        current_content.append(paragraph)
        current_len += paragraph_len

    if current_content:
        sections.append({
            "title": f"Section {section_id}",
            "content": "\n".join(current_content)
        })

    return sections


