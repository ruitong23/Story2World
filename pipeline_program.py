class UnionFind:
    def __init__(self, items):
        self.parent = {item: item for item in items}
        self.rank = {item: 0 for item in items}

    def find(self, item):
        parent = self.parent[item]
        if parent != item:
            self.parent[item] = self.find(parent)
        return self.parent[item]

    def union(self, left, right):
        root_left = self.find(left)
        root_right = self.find(right)
        if root_left == root_right:
            return
        if self.rank[root_left] < self.rank[root_right]:
            root_left, root_right = root_right, root_left
        self.parent[root_right] = root_left
        if self.rank[root_left] == self.rank[root_right]:
            self.rank[root_left] += 1

    def groups(self):
        result = defaultdict(list)
        for item in self.parent:
            result[self.find(item)].append(item)
        return [sorted(items) for items in result.values()]


def resolution_types_compatible(left_types, right_types):
    left = set(left_types)
    right = set(right_types)
    identity_types = {"Character", "TitleOrIdentity"}
    if not ((left | right) & identity_types):
        return False
    if left & right:
        return True
    return bool(
        ("Character" in left and "TitleOrIdentity" in right)
        or ("Character" in right and "TitleOrIdentity" in left)
    )

