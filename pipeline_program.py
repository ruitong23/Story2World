def mention_identity_role(mention):
    kind = mention.get("attributes", {}).get(
        "mention_kind", "descriptive_reference"
    )
    contexts = mention.get("contexts", [])
    if any(
        context.get("edge_type") == "ADDRESSES_AS"
        and context.get("role") == "target"
        for context in contexts
    ):
        return "address"
    if any(
        (
            context.get("edge_type") in {
                "TRANSFORMS_INTO", "IMPERSONATES"
            }
            and context.get("role") == "target"
        )
        or (
            context.get("edge_type") == "REVEALED_AS"
            and context.get("role") == "source"
        )
        for context in contexts
    ):
        return "form"
    if kind == "temporary_identity":
        return "temporary_identity"
    if kind == "named_entity":
        return "stable"
    return "reference"


def build_seed_clusters(index):
    mentions = index["mentions"]
    union_find = UnionFind(mentions)
    identity_candidates = []
    direct_evidence = []

    # Exact surfaces are merged only when every occurrence is a stable named
    # entity. A form, address, or generic reference with the same text remains
    # separate from an independently existing character.
    by_typed_surface = defaultdict(list)
    for node_id, mention in mentions.items():
        entity_type = mention["entity_type"]
        key = (
            entity_type,
            mention["normalized_surface"],
            (
                mention_identity_role(mention)
                if entity_type in {"Character", "TitleOrIdentity"}
                else "world_entity"
            ),
        )
        if key[1]:
            by_typed_surface[key].append(node_id)
    for (entity_type, _, identity_role), node_ids in by_typed_surface.items():
        # Non-character world entities with the same normalized name and type
        # are graph duplicates, not literary identity-resolution questions.
        # Character and identity surfaces retain the stricter stable-name rule.
        if (
            entity_type in {"Character", "TitleOrIdentity"}
            and identity_role != "stable"
        ):
            continue
        anchor = node_ids[0]
        for node_id in node_ids[1:]:
            union_find.union(anchor, node_id)

    reveal_targets = defaultdict(set)
    for link in index.get("identity_links", []):
        if link["edge_type"] != "REVEALED_AS":
            continue
        reveal_targets[(
            link["source_node_id"],
            normalize_surface_name(link.get("source_text", "")),
        )].add(link["target_node_id"])

    temporary_identity_neighbors = defaultdict(set)
    for link in index.get("identity_links", []):
        if link["edge_type"] not in {
            "TRANSFORMS_INTO", "REVEALED_AS", "HAS_ALIAS", "HAS_TITLE"
        }:
            continue
        source_id = link["source_node_id"]
        target_id = link["target_node_id"]
        source = mentions[source_id]
        target = mentions[target_id]
        source_role = mention_identity_role(source)
        target_role = mention_identity_role(target)
        if (
            source_role != "stable"
            and source.get("attributes", {}).get("mention_kind")
            != "named_entity"
            and target_role == "stable"
        ):
            temporary_identity_neighbors[source_id].add(target_id)
        if (
            target_role != "stable"
            and target.get("attributes", {}).get("mention_kind")
            != "named_entity"
            and source_role == "stable"
        ):
            temporary_identity_neighbors[target_id].add(source_id)

    conflicted_identity_bridges = {
        node_id
        for node_id, stable_node_ids in temporary_identity_neighbors.items()
        if len({
            mentions[stable_node_id]["normalized_surface"]
            for stable_node_id in stable_node_ids
            if mentions[stable_node_id]["normalized_surface"]
        }) > 1
    }

    for link in index.get("identity_links", []):
        source_id = link["source_node_id"]
        target_id = link["target_node_id"]
        source = mentions[source_id]
        target = mentions[target_id]
        edge_type = link["edge_type"]
        source_kind = source.get("attributes", {}).get("mention_kind", "")
        target_kind = target.get("attributes", {}).get("mention_kind", "")

        if (
            source_id in conflicted_identity_bridges
            or target_id in conflicted_identity_bridges
        ):
            # A shared disguise or ambiguous label linked to multiple stable
            # people must not become a transitive identity bridge.
            continue

        if edge_type == "TRANSFORMS_INTO":
            union_find.union(source_id, target_id)
            direct_evidence.append(link)
            continue

        if edge_type == "REVEALED_AS":
            reveal_key = (
                source_id,
                normalize_surface_name(link.get("source_text", "")),
            )
            if len(reveal_targets[reveal_key]) > 1:
                # One shared persona cannot make several distinct named
                # targets aliases of each other.
                continue
            if (
                source_kind != "named_entity"
                or source["entity_type"] == "TitleOrIdentity"
            ):
                union_find.union(source_id, target_id)
                direct_evidence.append(link)
            else:
                identity_candidates.append(link)
            continue

        if edge_type in {"HAS_ALIAS", "HAS_TITLE"} and (
            "TitleOrIdentity" in {
                source["entity_type"], target["entity_type"]
            }
            or "temporary_identity" in {source_kind, target_kind}
        ):
            union_find.union(source_id, target_id)
            direct_evidence.append(link)
            continue

        # Character-to-Character aliases and transformations need contextual
        # confirmation because extraction can confuse names and disguises.
        identity_candidates.append(link)

    return union_find, identity_candidates, direct_evidence

