def names_have_containment(left_names, right_names):
    for left in left_names:
        for right in right_names:
            if left == right:
                continue
            if min(len(left), len(right)) >= 2 and (left in right or right in left):
                return True
    return False


def names_have_ordered_subsequence(left_names, right_names):
    def is_subsequence(shorter, longer):
        if len(shorter) < 2 or len(shorter) >= len(longer):
            return False
        iterator = iter(longer)
        return (
            all(character in iterator for character in shorter)
            and len(shorter) / len(longer) >= 0.5
        )

    return any(
        is_subsequence(left, right) or is_subsequence(right, left)
        for left in left_names
        for right in right_names
        if left != right
    )


def names_have_one_character_variant(left_names, right_names):
    for left in left_names:
        for right in right_names:
            if left == right or len(left) != len(right) or len(left) < 3:
                continue
            if sum(a != b for a, b in zip(left, right)) == 1:
                return True
    return False


def build_candidate_pairs(cluster_summaries, node_to_cluster, identity_candidates):
    candidate_reasons = defaultdict(set)
    cluster_ids = sorted(cluster_summaries)
    stable_anchor_names = {
        name
        for summary in cluster_summaries.values()
        if "named_entity" in summary.get("mention_kinds", [])
        for name in summary.get("normalized_names", [])
        if len(name) >= 2
    }

    def description_anchors(summary):
        text = normalize_surface_name(" ".join(
            summary.get("descriptions", [])
        ))
        own_names = set(summary.get("normalized_names", []))
        return {
            name for name in stable_anchor_names
            if name not in own_names and name in text
        }

    by_name = defaultdict(list)
    by_context = defaultdict(list)
    for cluster_id, summary in cluster_summaries.items():
        for name in summary["normalized_names"]:
            by_name[name].append(cluster_id)
        for signature in summary["context_signatures"]:
            by_context[signature].append(cluster_id)

    for name, ids in by_name.items():
        for left, right in combinations(sorted(set(ids)), 2):
            left_roles = set(
                cluster_summaries[left].get("name_roles", {}).get(
                    name, []
                )
            )
            right_roles = set(
                cluster_summaries[right].get("name_roles", {}).get(
                    name, []
                )
            )
            if {"stable", "form"} <= (left_roles | right_roles):
                continue
            if resolution_types_compatible(
                cluster_summaries[left]["types"],
                cluster_summaries[right]["types"],
            ):
                candidate_reasons[(left, right)].add(f"same_surface:{name}")

    shared_context_counts = Counter()
    for ids in by_context.values():
        for left, right in combinations(sorted(set(ids)), 2):
            if resolution_types_compatible(
                cluster_summaries[left]["types"],
                cluster_summaries[right]["types"],
            ):
                shared_context_counts[(left, right)] += 1
    for pair, count in shared_context_counts.items():
        if count >= 2:
            candidate_reasons[pair].add(f"shared_graph_context:{count}")

    for left, right in combinations(cluster_ids, 2):
        left_summary = cluster_summaries[left]
        right_summary = cluster_summaries[right]
        if not resolution_types_compatible(
            left_summary["types"], right_summary["types"]
        ):
            continue
        left_named = "named_entity" in left_summary.get("mention_kinds", [])
        right_named = "named_entity" in right_summary.get("mention_kinds", [])
        if not (left_named or right_named):
            continue

        if names_have_containment(
            left_summary["normalized_names"],
            right_summary["normalized_names"],
        ):
            candidate_reasons[(left, right)].add(
                "lexical_name_containment"
            )
            if shared_context_counts[(left, right)]:
                candidate_reasons[(left, right)].add(
                    "name_containment_plus_graph_context"
                )
        elif names_have_ordered_subsequence(
            left_summary["normalized_names"],
            right_summary["normalized_names"],
        ):
            candidate_reasons[(left, right)].add(
                "ordered_name_subsequence"
            )
        elif names_have_one_character_variant(
            left_summary["normalized_names"],
            right_summary["normalized_names"],
        ):
            candidate_reasons[(left, right)].add(
                "one_character_name_variant"
            )

        left_chunks = [int(item) for item in left_summary["chunk_ids"]]
        right_chunks = [int(item) for item in right_summary["chunk_ids"]]
        adjacent_chunks = bool(
            left_chunks and right_chunks
            and min(
                abs(left_chunk - right_chunk)
                for left_chunk in left_chunks
                for right_chunk in right_chunks
            ) <= 1
        )
        shared_name_prefix = any(
            left_name and right_name and left_name[0] == right_name[0]
            for left_name in left_summary["normalized_names"]
            for right_name in right_summary["normalized_names"]
        )
        shared_anchors = sorted(
            description_anchors(left_summary)
            & description_anchors(right_summary)
        )
        if (
            left_named and right_named
            and "Character" in left_summary["types"]
            and "Character" in right_summary["types"]
            and adjacent_chunks
            and shared_name_prefix
            and shared_anchors
        ):
            candidate_reasons[(left, right)].add(
                "adjacent_role_continuity:"
                + "|".join(shared_anchors[:4])
            )

    for bridge_id, bridge_summary in cluster_summaries.items():
        if (
            "Character" not in bridge_summary.get("types", [])
            or "descriptive_reference"
                not in bridge_summary.get("mention_kinds", [])
        ):
            continue
        bridge_names = [
            name for name in bridge_summary.get("normalized_names", [])
            if name
        ]
        bridge_has_identity_context = any(
            context.get("edge_type") in {
                "HAS_ALIAS", "HAS_TITLE", "HAS_FORMER_IDENTITY",
                "TRANSFORMS_INTO", "REVEALED_AS", "IMPERSONATES",
            }
            for context in bridge_summary.get("contexts", [])
        )
        if not bridge_names or not bridge_has_identity_context:
            # A descriptive mention that merely appears in several character
            # summaries is co-reference evidence at most. It cannot bridge two
            # independently named actors into one identity by itself.
            continue
        referencing_named_clusters = []
        for cluster_id, summary in cluster_summaries.items():
            if cluster_id == bridge_id:
                continue
            if (
                "Character" not in summary.get("types", [])
                or "named_entity" not in summary.get("mention_kinds", [])
            ):
                continue
            narrative_text = normalize_surface_name(" ".join(
                summary.get("descriptions", [])
                + summary.get("source_texts", [])
            ))
            if any(name in narrative_text for name in bridge_names):
                referencing_named_clusters.append(cluster_id)
        if not (2 <= len(referencing_named_clusters) <= 4):
            continue
        bridge_label = bridge_summary.get("names", [""])[0]
        for left, right in combinations(
            sorted(referencing_named_clusters), 2
        ):
            candidate_reasons[(left, right)].add(
                f"shared_identity_bridge:{bridge_label}"
            )

    named_character_clusters = [
        (cluster_id, summary)
        for cluster_id, summary in cluster_summaries.items()
        if "Character" in summary.get("types", [])
        and "named_entity" in summary.get("mention_kinds", [])
    ]
    for left, right in combinations(named_character_clusters, 2):
        left_id, left_summary = left
        right_id, right_summary = right
        if not (
            set(left_summary.get("chunk_ids", []))
            & set(right_summary.get("chunk_ids", []))
        ):
            continue
        left_description = normalize_surface_name(" ".join(
            left_summary.get("descriptions", [])
        ))
        right_description = normalize_surface_name(" ".join(
            right_summary.get("descriptions", [])
        ))
        left_names = [
            name for name in left_summary.get("normalized_names", [])
            if len(name) >= 2
        ]
        right_names = [
            name for name in right_summary.get("normalized_names", [])
            if len(name) >= 2
        ]
        left_description_names_both = (
            any(name in left_description for name in left_names)
            and any(name in left_description for name in right_names)
        )
        right_description_names_both = (
            any(name in right_description for name in right_names)
            and any(name in right_description for name in left_names)
        )
        if left_description_names_both or right_description_names_both:
            pair = tuple(sorted((left_id, right_id)))
            candidate_reasons[pair].add("description_name_reference")

    for link in identity_candidates:
        left = node_to_cluster[link["source_node_id"]]
        right = node_to_cluster[link["target_node_id"]]
        if left == right:
            continue
        pair = tuple(sorted((left, right)))
        evidence = clean_graph_text(link.get("source_text", ""))
        candidate_reasons[pair].add(
            f"explicit_{link['edge_type']}:{evidence}"
        )

    def address_only(summary):
        contexts = summary.get("contexts", [])
        return (
            bool(contexts)
            and "named_entity" not in summary.get("mention_kinds", [])
            and all(
                context["edge_type"] == "ADDRESSES_AS"
                and context["role"] == "target"
                for context in contexts
            )
        )

    candidates = []
    for (left, right), reasons in sorted(candidate_reasons.items()):
        left_summary = cluster_summaries[left]
        right_summary = cluster_summaries[right]
        material_reasons = [
            reason
            for reason in reasons
            if not reason.startswith("shared_graph_context:")
        ]
        if not material_reasons:
            continue
        explicit_identity = any(
            reason.startswith("explicit_")
            for reason in reasons
        )
        if address_only(left_summary) or address_only(right_summary):
            continue
        if not (
            "named_entity" in left_summary.get("mention_kinds", [])
            or "named_entity" in right_summary.get("mention_kinds", [])
            or explicit_identity
        ):
            continue
        shared_contexts = sorted(
            set(left_summary["context_signatures"])
            & set(right_summary["context_signatures"])
        )
        pair_id = "pair_" + hashlib.sha1(
            f"{left}|{right}".encode("utf-8")
        ).hexdigest()[:12]
        candidates.append({
            "pair_id": pair_id,
            "left_cluster_id": left,
            "right_cluster_id": right,
            "reasons": sorted(reasons),
            "shared_contexts": shared_contexts,
        })
    return candidates


