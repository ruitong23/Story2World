def mention_has_context(mention, edge_type, role=None):
    return any(
        context.get("edge_type") == edge_type
        and (role is None or context.get("role") == role)
        for context in mention.get("contexts", [])
    )


def classify_entity_names(group_node_ids, mentions):
    buckets = {
        "stable_names": set(),
        "titles": set(),
        "forms": set(),
        "temporary_identities": set(),
        "generic_references": set(),
        "address_terms": set(),
    }
    form_subtypes = {
        "form", "transformation_form", "appearance", "avatar", "disguise"
    }
    for node_id in group_node_ids:
        mention = mentions[node_id]
        name = mention["surface_name"]
        kind = mention.get("attributes", {}).get(
            "mention_kind", "descriptive_reference"
        )
        entity_type = mention["entity_type"]
        identity_subtype = clean_graph_text(
            mention.get("attributes", {}).get("identity_subtype", "")
        ).casefold()
        contexts = mention.get("contexts", [])
        is_address = (
            kind != "named_entity"
            and bool(contexts)
            and all(
                context.get("edge_type") == "ADDRESSES_AS"
                and context.get("role") == "target"
                for context in contexts
            )
        )
        is_form = (
            identity_subtype in form_subtypes
            or any(
                context.get("edge_type") == "REVEALED_AS"
                and context.get("role") == "source"
                for context in contexts
            )
            or any(
                context.get("edge_type") == "TRANSFORMS_INTO"
                and context.get("role") == "target"
                for context in contexts
            )
            or (
                kind == "temporary_identity"
                and any(
                    context.get("edge_type") == "IMPERSONATES"
                    and context.get("role") == "target"
                    for context in contexts
                )
            )
        )
        is_title = (
            mention_has_context(mention, "HAS_TITLE", "target")
            or (
                entity_type == "TitleOrIdentity"
                and identity_subtype == "title"
            )
        )
        is_stable_alias = (
            entity_type == "Character"
            and mention_has_context(mention, "HAS_ALIAS")
            and not is_form
            and not is_address
        )

        if is_form:
            buckets["forms"].add(name)
        elif is_title:
            buckets["titles"].add(name)
        elif is_address:
            buckets["address_terms"].add(name)
        elif is_stable_alias:
            buckets["stable_names"].add(name)
        elif kind == "temporary_identity":
            buckets["temporary_identities"].add(name)
        elif kind in {
            "generic_role", "collective", "descriptive_reference"
        }:
            buckets["generic_references"].add(name)
        elif kind == "named_entity":
            buckets["stable_names"].add(name)
        else:
            buckets["generic_references"].add(name)

    for stable_name in buckets["stable_names"]:
        for bucket_name in (
            "titles", "forms", "temporary_identities",
            "generic_references", "address_terms",
        ):
            buckets[bucket_name].discard(stable_name)

    return {
        key: sorted(values)
        for key, values in buckets.items()
    }

def choose_stable_canonical_name(group_node_ids, mentions, name_buckets):
    stable_names = set(name_buckets["stable_names"])
    if not stable_names:
        return choose_canonical_name(group_node_ids, mentions, [])
    occurrence_count = Counter(
        mentions[node_id]["surface_name"]
        for node_id in group_node_ids
        if mentions[node_id]["surface_name"] in stable_names
    )
    repeated_names = {
        name for name in stable_names
        if occurrence_count[name] >= 2
    }
    candidates = repeated_names or stable_names
    return sorted(
        candidates,
        key=lambda name: (
            -len(name),
            -occurrence_count[name],
            name,
        ),
    )[0]


def classify_reference_entity(group_node_ids, mentions):
    group_mentions = [mentions[node_id] for node_id in group_node_ids]
    kinds = {
        mention.get("attributes", {}).get(
            "mention_kind", "descriptive_reference"
        )
        for mention in group_mentions
    }
    has_role_signal = any(
        mention.get("attributes", {}).get("role_in_scene")
        or mention.get("attributes", {}).get("entity_subtype")
        for mention in group_mentions
    )
    context_count = sum(
        len(mention.get("contexts", []))
        for mention in group_mentions
    )
    if "collective" in kinds:
        reference_kind = "collective"
        review_status = "context_only"
    elif has_role_signal or context_count >= 2:
        reference_kind = "potential_minor_character"
        review_status = "review_for_promotion"
    else:
        reference_kind = "generic_character_reference"
        review_status = "context_only"
    return reference_kind, review_status, context_count

def build_canonical_output(
    graph,
    index,
    cluster_summaries,
    candidates,
    decisions,
    direct_evidence,
):
    mentions = index["mentions"]
    union_find = UnionFind(mentions)
    decision_by_pair = {item["pair_id"]: item for item in decisions}
    candidate_by_pair = {
        item["pair_id"]: item
        for item in candidates
    }

    for summary in cluster_summaries.values():
        node_ids = summary["node_ids"]
        for node_id in node_ids[1:]:
            union_find.union(node_ids[0], node_id)

    def cluster_is_address_only(summary):
        contexts = summary.get("contexts", [])
        return (
            bool(contexts)
            and "named_entity" not in summary.get("mention_kinds", [])
            and all(
                context["edge_type"] == "ADDRESSES_AS"
                and context["role"] == "target"
                for context in contexts
            )
        )

    stable_catalog_names = {
        name
        for summary in cluster_summaries.values()
        if set(summary.get("types", []))
        & {"Character", "TitleOrIdentity"}
        for name in summary.get("normalized_names", [])
        if "stable" in summary.get("name_roles", {}).get(name, [])
    }
    contained_name_expansions = defaultdict(set)
    for short_name in stable_catalog_names:
        for long_name in stable_catalog_names:
            if short_name != long_name and short_name in long_name:
                contained_name_expansions[short_name].add(long_name)
    ambiguous_contained_names = {
        name
        for name, expansions in contained_name_expansions.items()
        if len(expansions) > 1
        and any(
            left != right
            and left not in right
            and right not in left
            for left in expansions
            for right in expansions
        )
    }

    def merge_is_supported(candidate, decision):
        reasons = candidate["reasons"]
        left = cluster_summaries[candidate["left_cluster_id"]]
        right = cluster_summaries[candidate["right_cluster_id"]]
        if cluster_is_address_only(left) or cluster_is_address_only(right):
            return False
        same_surface = bool(
            set(left["normalized_names"])
            & set(right["normalized_names"])
        )
        shared_names = (
            set(left["normalized_names"])
            & set(right["normalized_names"])
        )

        def stable_names(summary):
            return {
                name
                for name in summary.get("normalized_names", [])
                if "stable" in summary.get("name_roles", {}).get(name, [])
            }

        left_distinct_stable = stable_names(left) - shared_names
        right_distinct_stable = stable_names(right) - shared_names
        stable_names_compatible = any(
            left_name in right_name or right_name in left_name
            for left_name in left_distinct_stable
            for right_name in right_distinct_stable
        ) or names_have_ordered_subsequence(
            left_distinct_stable,
            right_distinct_stable,
        )
        distinct_stable_name_conflict = (
            bool(left_distinct_stable)
            and bool(right_distinct_stable)
            and not stable_names_compatible
        )
