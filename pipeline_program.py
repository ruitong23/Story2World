def enrich_character_state(character):
    compact = {
        "canonical_name": character["canonical_name"],
        "aliases": character["aliases"],
        "background": character["background"][:12],
        "relations": [
            {
                "type": item["relation_type"],
                "direction": item["direction"],
                "name": item["name"],
                "evidence": [
                    evidence["source_text"]
                    for evidence in item["evidence"][:3]
                ],
            }
            for item in character["all_relations"][:24]
        ],
        "evidence": [
            item["source_text"]
            for item in character["evidence"][:18]
        ],
    }
    prompt = f"""
{language_output_instruction(
    character.get("data_status", {}).get("language_profile")
)}

Create a concise state update for this character.
Return:
{{
  "background_summary": "1-3 evidence-bound sentences",
  "personality": ["observed trait"],
  "speech_styles": ["observed speech feature"],
  "goals": ["explicit goal"],
  "constraints": ["explicit limitation"],
  "knowledge_scope": ["explicitly known information"]
}}

Character evidence:
{json.dumps(compact, ensure_ascii=False, indent=2)}
""".strip()
    payload = None
    feedback = ""
    last_error = None
    for _ in range(STEP14_ENRICH_RETRIES + 1):
        attempt_prompt = prompt
        if feedback:
            attempt_prompt += (
                "\n\nThe previous response was invalid. Return one complete "
                "JSON object only. Validation error: " + feedback
            )
        raw = call_llm(
            STEP14_ENRICH_SYSTEM_PROMPT,
            attempt_prompt,
            temperature=0.0,
            max_tokens=1200,
        )
        try:
            candidate_payload = extract_json_from_llm_text(raw)
            if not isinstance(candidate_payload, dict):
                raise ValueError("response is not a JSON object")
            payload = candidate_payload
            break
        except Exception as exc:
            last_error = exc
            feedback = clean_graph_text(str(exc))
    if not isinstance(payload, dict):
        character["enrichment_status"] = "failed"
        character["enrichment_error"] = clean_graph_text(last_error)
        return character
    character["enrichment_status"] = "complete"
    character.pop("enrichment_error", None)
    character["background_summary"] = clean_graph_text(
        payload.get("background_summary", "")
    )
    if (
        character["background_summary"]
        and not text_matches_language(
            character["background_summary"],
            character["data_status"]["language_profile"],
        )
    ):
        character["background_summary"] = ""
    for field in ("personality", "speech_styles", "goals", "constraints", "knowledge_scope"):
        values = payload.get(field, [])
        if isinstance(values, list):
            character[field] = [
                clean_graph_text(value)
                for value in values
                if clean_graph_text(value)
                and text_matches_language(
                    clean_graph_text(value),
                    character["data_status"]["language_profile"],
                )
            ][:10]
    return character


