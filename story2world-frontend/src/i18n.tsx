type TranslationKey = keyof typeof messages.en;

function detectLanguage(): Language {
  const saved = localStorage.getItem(LANGUAGE_KEY);
  if (saved === "en" || saved === "zh") return saved;
  const languages = navigator.languages?.length
    ? navigator.languages
    : [navigator.language];
  return languages.some((language) => language.toLowerCase().startsWith("zh"))
    ? "zh"
    : "en";
}

export function getCurrentLanguage(): Language {
  return detectLanguage();
}

function interpolate(text: string, variables?: Record<string, string | number>) {
  if (!variables) return text;
  return Object.entries(variables).reduce(
    (result, [key, value]) =>
      result.replaceAll(`{{${key}}}`, String(value)),
    text,
  );
}

export function translateCurrent(
  key: TranslationKey,
  variables?: Record<string, string | number>,
) {
  const language = detectLanguage();
  return interpolate(messages[language][key] || messages.en[key], variables);
}

interface LanguageContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (
    key: TranslationKey,
    variables?: Record<string, string | number>,
  ) => string;
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(detectLanguage);

  const setLanguage = (next: Language) => {
    localStorage.setItem(LANGUAGE_KEY, next);
    setLanguageState(next);
  };

  useEffect(() => {
    document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
    document.title =
      language === "zh"
        ? "Story2World · 小说世界引擎"
        : "Story2World · Author World Engine";
  }, [language]);

  const value = useMemo<LanguageContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, variables) =>
        interpolate(messages[language][key] || messages.en[key], variables),
    }),
    [language],
  );

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }
  return context;
}

export function LanguageSwitcher({
  prominent = false,
}: {
  prominent?: boolean;
}) {
  const { language, setLanguage, t } = useLanguage();
  return (
    <div
      className={`language-switcher ${prominent ? "prominent" : ""}`}
      aria-label={t("language.label")}
    >
      {prominent && <span>{t("language.label")}</span>}
      <button
        type="button"
        className={language === "en" ? "active" : ""}
        onClick={() => setLanguage("en")}
        aria-pressed={language === "en"}
      >
        EN
      </button>
      <button
        type="button"
        className={language === "zh" ? "active" : ""}
        onClick={() => setLanguage("zh")}
        aria-pressed={language === "zh"}
      >
        中文
      </button>
    </div>
  );
}
