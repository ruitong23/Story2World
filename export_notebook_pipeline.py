def emit_progress(percent, label, current=None, total=None):
    payload = {
        "percent": max(0.0, min(100.0, float(percent))),
        "label": str(label),
    }
    if current is not None:
        payload["current"] = int(current)
    if total is not None:
        payload["total"] = int(total)
    _ORIGINAL_PRINT(
        "@@PROGRESS " + _progress_json.dumps(payload, ensure_ascii=False),
        flush=True,
    )


def _pipeline_print(*values, **kwargs):
    text = " ".join(str(value) for value in values)
    chunk_match = _progress_re.search(r"Step 9: chunk (\d+)/(\d+)", text)
    batch_match = _progress_re.search(r"Step 11: batch (\d+)/(\d+)", text)
    if chunk_match:
        current, total = map(int, chunk_match.groups())
        start, end = _STEP9_RANGE
        emit_progress(
            start + (end - start) * current / max(1, total),
            f"Step 9 graph extraction: chunk {current}/{total}",
            current,
            total,
        )
    elif batch_match:
        current, total = map(int, batch_match.groups())
        start, end = _STEP11_RANGE
        emit_progress(
            start + (end - start) * current / max(1, total),
            f"Step 11 identity resolution: batch {current}/{total}",
            current,
            total,
        )
    _ORIGINAL_PRINT(*values, **kwargs)


builtins.print = _pipeline_print
