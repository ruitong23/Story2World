def prepare_cell_source(cell_index, source):
    if cell_index == 16:
        if LLM_CLIENT_BLOCK not in source or LLM_INIT_BLOCK not in source:
            raise RuntimeError("Notebook LLM client block changed; update the exporter.")
        return source.replace(
            LLM_CLIENT_BLOCK,
            LLM_CLIENT_REPLACEMENT,
        ).replace(
            LLM_INIT_BLOCK,
            LLM_INIT_REPLACEMENT,
        )
    if cell_index == 22:
        if STEP9_RESULT_BLOCK not in source or STEP9_STATE_BLOCK not in source:
            raise RuntimeError("Notebook Step 9 blocks changed; update the exporter.")
        return source.replace(
            STEP9_RESULT_BLOCK,
            STEP9_RESULT_REPLACEMENT,
        ).replace(
            STEP9_STATE_BLOCK,
            STEP9_STATE_REPLACEMENT,
        )
    return source


def main():
    notebook = json.loads(NOTEBOOK.read_text(encoding="utf-8"))
    parts = [HEADER]
    for kind, value in CELL_SEQUENCE:
        if kind == "cell":
            source = "".join(notebook["cells"][value].get("source", []))
            source = prepare_cell_source(value, source)
            parts.append(f"\n\n# %% Notebook cell {value}\n{source.rstrip()}\n")
        elif kind == "progress":
            percent, label = value
            parts.append(f'\n\nemit_progress({percent}, {label!r})\n')
        else:
            parts.append(f"\n\n{value.strip()}\n")
    parts.append(FOOTER)
    OUTPUT.write_text("".join(parts), encoding="utf-8")
    print(f"Exported {OUTPUT} ({OUTPUT.stat().st_size:,} bytes)")


if __name__ == "__main__":
    main()
